# Defines action prompts and output contracts.


# ========
# Defines suggest candidates function for this module workflow.
# ========
def suggest_stakeholders(*, scenario_context: str) -> str:
    return f"""# 任務
根據以下產品情境，建議真正會影響需求分析的利害關係人。
先判斷這個產品有哪些不同需求視角，再把每個角色歸到 type。

# 產品情境
{scenario_context}

- primary_user：每天直接操作系統、輸入資料、接收通知或完成任務的人。
- system_owner：負責派工、監督流程、營運決策、權限、資料品質、系統穩定性、安全或維護的人。
- external_party：外部會影響或受影響的單位。

- type 只是粗分類；選擇角色時以「不同需求視角」為主，不要只為了平均分類而取角色。
- 至少輸出 4 位利害關係人；一般產品通常 5-8 位，多方平台、交易平台或營運流程複雜的產品通常 6-9 位。
- 若產品情境真的很小，可以少於 4 位，但 reason 必須說明為何沒有更多會影響需求判斷的角色。
- 若產品情境只是數字、代號、單字或資訊不足，不能判斷系統類型時，不要推測成常見系統，也不要產生「客服人員」「服務接受者」等泛用角色；只輸出最小可用角色，例如需求提出者與系統負責人，並在 reason 說明需要先釐清產品情境。
- 優先考慮以下需求視角；有直接關聯就納入，沒有就不要硬湊：
  1. 直接使用者：每天操作系統、完成任務、接收通知或輸入資料的人。
  2. 系統/業務管理者：負責設定、監控、審核、派工、營運決策、權限或資料品質的人。
  3. 交易或服務對象：會下單、付款、查詢、收取服務、申訴、評價或受服務結果影響的人。
  4. 外部合作或依賴方：提供商品、服務、金流、物流、身份驗證、資料、合約或作業配合的單位。
  5. 支援與例外處理角色：負責客服、退款、帳務、異常處理、人工補救或稽核的人。
  6. 合規與治理角色：會影響法規、隱私、安全、資料保存、稽核、契約或政策限制的人或單位。
- 每位利害關係人必須直接存在於產品情境中，且會影響需求、限制、責任邊界、驗收、風險或決策。
- 每位利害關係人的日常使用情境、責任邊界或決策觀點要明確且不同。
- 避免使用情境重疊；若兩個角色的日常任務、需求目標、風險與驗收觀點幾乎相同，合併。
- 若兩個角色會造成不同需求、限制、責任邊界或驗收條件，即使同屬一個 type 也要保留。
- 不要只因為都是 external_party 就合併，也不要把明顯不同的交易方合併成泛稱。
- name 只填名稱，不要用括號補充說明。
- type 只能是 primary_user、system_owner、external_party。
- reason 用一句話說明此角色會帶來哪種不同的需求、限制、責任邊界、驗收、風險或決策觀點。

{{
    "proposed_stakeholders": [
        {{"name": "利害關係人名稱", "type": "primary_user | system_owner | external_party", "reason": "一句話選擇理由"}}
    ]
}}"""


# ========
# Defines write stakeholder text function for this module workflow.
# ========
def write_stakeholder_text(
    *,
    stakeholder_list: str,
    scenario_context: str,
) -> str:
    return f"""# 任務
模擬以下利害關係人，以第一人稱、口語方式從各自角度提出需求。

# 利害關係人
{stakeholder_list}

# 產品情境
{scenario_context}

1. 日常使用情境
2. 痛點與困擾
3. 期望功能
4. 擔心的事
5. 最在意的限制、底線或不可接受情況
6. 與其他角色可能產生取捨的地方

- 每位利害關係人產生 3-5 條 text。
- 只根據該利害關係人的日常經驗。
- 不替未選中的角色發言。
- 每條 text 都必須能回扣產品情境。
- 請自然描述該角色的目標、擔憂、限制、底線與可接受/不可接受的取捨。
- 不要刻意製造衝突；只有在產品情境中合理時，才描述可能與其他角色目標拉扯的地方。

{{
    "stakeholders": [
        {{
            "name": "利害關係人名稱",
            "text": ["第一人稱需求發言"]
        }}
    ]
}}"""
